// ---- Letter: typewriter (long text + auto scroll) ----
function buildLetter() {
  const wrap = document.createElement("div");

  const paper = document.createElement("div");
  paper.className = "paper paper--scroll";

  const hand = document.createElement("div");
  hand.className = "hand";
  hand.textContent = "";
  paper.appendChild(hand);

  const caret = document.createElement("span");
  caret.className = "caret";
  hand.appendChild(caret);

  wrap.appendChild(paper);

  let i = 0;
  const text = normalizeLetter(CONFIG.LETTER_TEXT);

  // Auto-scroll follows typing unless user scrolls up.
  let autoScroll = true;
  const bottomThreshold = 28;

  function isAtBottom() {
    return (paper.scrollTop + paper.clientHeight) >= (paper.scrollHeight - bottomThreshold);
  }

  function maybeScroll() {
    if (!autoScroll) return;
    paper.scrollTop = paper.scrollHeight;
  }

  paper.addEventListener("scroll", () => {
    autoScroll = isAtBottom();
  }, { passive: true });

  // Start typing after modal animation
  let timer = null;
  let tick = 0;

  function type() {
    if (!modal.classList.contains("open")) return;

    if (i < text.length) {
      caret.insertAdjacentText("beforebegin", text.charAt(i));
      i++;
      tick++;

      // scroll every few chars to keep it smooth
      if (tick % 3 === 0) requestAnimationFrame(maybeScroll);

      timer = setTimeout(type, CONFIG.TYPE_SPEED_MS);
    } else {
      caret.remove();
      requestAnimationFrame(() => { paper.scrollTop = paper.scrollHeight; });
    }
  }

  timer = setTimeout(() => {
    paper.scrollTop = 0;
    autoScroll = true;
    type();
  }, 420);

  const observer = new MutationObserver(() => {
    if (!modal.classList.contains("open")) {
      if (timer) clearTimeout(timer);
      observer.disconnect();
    }
  });
  observer.observe(modal, { attributes: true, attributeFilter: ["class"] });

  return wrap;
}

