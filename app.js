function normalizeLetter(raw) {
  // Remove accidental indentation at start of lines (common when copy/paste)
  const t = String(raw ?? "").replace(/\r/g, "");
  return t
    .split("\n")
    .map(line => line.replace(/\t/g, " ").replace(/^\s+/, "").replace(/\s+$/, ""))
    .join("\n")
    .replace(/\n{3,}/g, "\n\n");
}

