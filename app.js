function openModal(node) {
  modalContent.innerHTML = "";
  modalContent.appendChild(node);
  modal.setAttribute("aria-hidden", "false");
  // next frame to ensure transitions fire smoothly
  requestAnimationFrame(() => modal.classList.add("open"));
}
function closeModal() {
  modal.classList.remove("open");
  modal.setAttribute("aria-hidden", "true");
  // wait for close transition then clear
  setTimeout(() => { if (!modal.classList.contains("open")) modalContent.innerHTML = ""; }, 220);
}
modal.addEventListener("click", (e) => { if (e.target?.dataset?.close) closeModal(); });
window.addEventListener("keydown", (e) => { if (e.key === "Escape" && modal.classList.contains("open")) closeModal(); });

// Helpers
function withFallback(img, src) {

  img.src = src;
  img.onerror = () => img.src = src.replace(".png", ".svg").replace(".jpg", ".svg");
}

function swapImg(imgEl, src) {
  // crossfade
  imgEl.classList.add("imgFade");
  const onLoad = () => {
    imgEl.classList.remove("imgFade");
    imgEl.removeEventListener("load", onLoad);
  };
  imgEl.addEventListener("load", onLoad);
  withFallback(imgEl, src);
}

