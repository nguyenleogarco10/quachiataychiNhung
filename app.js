function buildKeypad() {
  keypad.innerHTML = "";

  const grid = document.createElement("div");
  grid.className = "keypadGrid";

  const digits = ["1", "2", "3", "4", "5", "6", "7", "8", "9", "empty", "0", "empty"];
  digits.forEach(k => {
    const b = document.createElement("button");
    b.type = "button";

    if (k === "empty") {
      b.className = "key key--empty";
      b.disabled = true;
    } else {
      b.className = "key";
      b.textContent = k;
      b.onclick = () => {
        haptic(15);
        tryStartMusic();

        if (pin.length >= 4) return;
        pin += k;
        renderDots();

        if (pin.length === 4) {
          if (pin === CONFIG.PASSWORD) {
            flash("Unlocked ✨");
            setTimeout(unlock, 200);
          } else {
            flash("Sai mật khẩu 😅");
            setTimeout(resetPin, 220);
          }
        }
      };
    }
    grid.appendChild(b);
  });

  const back = document.createElement("button");
  back.type = "button";
  back.className = "keyBack";
  back.textContent = "×";
  back.onclick = () => {
    haptic(10);
    tryStartMusic();
    pin = pin.slice(0, -1);
    renderDots();
  };

  keypad.appendChild(grid);
  keypad.appendChild(back);
}

